package quiz;

public class Quiz2 {

	public static void main(String[] args) {
		int gameLevel; //
		// gameLevel이라는 변수를 카멜기법으로 선언		
		gameLevel = 10; //
		// 게임레벨의 변수의 값을 10으로 정함
		System.out.println(gameLevel); //
		// 게임레벨의 값인 10을 확인가능하도록 표출
		
		int busNumber = 9; 
		// busNumber라는 변수 선언과 동시에 9라는 값을 배정
		busNumber = 111; 
		// busNumber = 9를 111로 변경
		

	}

}
